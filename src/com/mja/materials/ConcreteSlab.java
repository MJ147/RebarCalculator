package com.mja.materials;

public class ConcreteSlab {

    private int slabHeight;
    private double computationalConcreteStrength;
    private int concreteCover;

    public int getSlabHeight() {
        return slabHeight;
    }

    public void setSlabHeight(int slabHeight) {
        this.slabHeight = slabHeight;
    }

    public double getComputationalConcreteStrength() {
        return computationalConcreteStrength;
    }

    public void setComputationalConcreteStrength(double computationalConcreteStrength) {
        this.computationalConcreteStrength = computationalConcreteStrength;
    }

    public int getConcreteCover() {
        return concreteCover;
    }

    public void setConcreteCover(int concreteCover) {
        this.concreteCover = concreteCover;
    }
}
